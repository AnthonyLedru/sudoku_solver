#include "sudoku_solver.h"

int		comp_tab(int s1[9][9], int s2[9][9])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		while (j < 9)
		{
			if (s1[i][j] != s2[i][j])
				return (0);
			j++;
		}
		i++;
		j = 0;
	}
	return (1);
}
